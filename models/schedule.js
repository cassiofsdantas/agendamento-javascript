//classe que representa o objeto de um agendamento
class Schedule 
{
    constructor(name, phone, services, date, time, price, status)
    {
        this.name = name;
        this.phone = phone;
        this.services = services;
        this.date = date;
        this.time = time;
        this.price = price;
        this.status = status;
    }
}